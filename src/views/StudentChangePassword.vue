<script setup lang="ts">
import router from '@/router'
import { useSystemStore } from '@/stores/counter'
import { ref } from 'vue'
interface EmitType {
  (e: 'emitClose', data: boolean): void
}
const emit = defineEmits<EmitType>()
const close = () => emit('emitClose', false)
const userName = sessionStorage.getItem('token')
const password = ref('')
const newPassword = ref('')
const store = useSystemStore()
const submit = () => {
  if (
    password.value != newPassword.value ||
    !password.value ||
    !newPassword.value
  ) {
    alert('密码相同改一下喵~')
    password.value = ''
    newPassword.value = ''
  } else {
    store.changeStudentPassword(userName ?? '', password.value)
    router.push('/')
  }
}
</script>
<template>
  <div class="modal" @click="close">
    <div class="modal-dialog" @click.stop>
      <div class="modal-header">
        <p>用户id：{{ userName }}</p>
        <input
          type="password"
          required
          placeholder="新密码"
          v-model="password"
          style="height: 40px; width: 200px"
        />
        <input
          type="password"
          id="2"
          required
          placeholder="确认密码"
          v-model="newPassword"
          style="height: 40px; width: 200px"
        />
        <br />
        <br />
        <div class="modal-footer">
          <botton class="btn btn-primary" @click="submit">修改</botton>
          <br />
        </div>
        <br />
      </div>
    </div>
  </div>
</template>
